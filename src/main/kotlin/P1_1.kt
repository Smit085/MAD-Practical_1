//1.Store & Display values in different variable of different type
// (Integer, Double, Float, Long, Short, Byte, Char, Boolean, String)
fun main(args: Array<String>) {
    println("Types of Data Types in Kotlin:")
    val enr_no = 5
    val weight = 57.557
    val a = 'A'
    val b = true
    val name = "Smit"
    val c = 57.8
    val d: Short = -85
    val e: Long = 124342334
    val f: Byte = 127
    println("Integer Type: $enr_no")
    println("Float Type: $c")
    println("Character Type: $a")
    println("String Type: $name")
    println("Boolean Type: $b")
    println("Double Type: $weight")
    println("Long Type: $e")
    println("Short Value: $d")
    println("Byte Value: $f")
}
